function toggleSection(secId, evt)
{
  var newParms;
  var parentNode = (evt.target) ? evt.target : evt.srcElement;
  var el = document.getElementById(secId);
  //alert(el.style.display);
  if (el.style.display=='') {
    el.style.display='none';
    newParms = showParms;
  }
  else  {
    el.style.display='';
    newParms = hideParms;
  }
  updateControlNode(parentNode, newParms)
}


